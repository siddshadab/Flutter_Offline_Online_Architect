

class LosResponseBean{

  int errorFlag;
  String errorMsg;



  LosResponseBean({this.errorFlag, this.errorMsg});


  factory LosResponseBean.fromMap(Map<String, dynamic> map) {

    return LosResponseBean(
      errorFlag : 		map["errorFlag"] as int,
      errorMsg : 		  map["errorMsg"] as String,

    );
  }
}


